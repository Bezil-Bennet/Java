package week1.day1;


public class If {
public static void main(String[] args) {
	int a = 5, b = 10;
	//if(condition){trueBlock}else{FalseBlock}
	if(a>b) {
		System.out.println("A"+a);
	}else {
		System.out.println("B="+b);
	}
	
	
	
	int num = 20;
	if (num%2==0) {
		System.out.println("The Number is Even");
		}
	else {
		System.out.println("Odd number");
	}
	
}
}
